class _11CompoundAssignmentOperator
{
	public static void main(String[] args) 
	{
		int sal = 5000;
		System.out.println(sal);

		sal += 3000;
		System.out.println(sal);

		int bal = 15000;
		System.out.println(bal);

		bal *= 3;
		System.out.println(bal);

		//Narrowing (their is no need of explicit typecasting with the help of compount assignment operator)

		int num = 100;
		System.out.println(num);

		num += 12.6723;
		System.out.println(num);

		char ch = 'a';
		System.out.println(ch);

		ch += 200.46;
		System.out.println(ch);

		
	}
}