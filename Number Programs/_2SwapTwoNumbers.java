class _2SwapTwoNumbers
{
	public static void main(String[] args)
	{
	
	int a = 10;
	int b = 20;
	
	System.out.println("Before Swapping : ");
	System.out.println(a);
	System.out.println(b);
	System.out.println();

	int temp = a;
	a = b;
	b = temp;

	System.out.println("After Swapping");
	System.out.println(a);
	System.out.println(b);
	}

}